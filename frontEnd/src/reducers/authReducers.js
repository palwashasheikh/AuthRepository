import {
  LOGIN_SUCCESS,
  SIGNUP_SUCCESS,
  LOGOUT,
  AUTH_ERROR,
  LOGIN_FAIL,
  SIGNUP_FAIL,
  USER_LOADED,
  // other action types
} from '../actions/types';

const initialState = {
  token: localStorage.getItem('token'),
  isAuthenticated: null,
  loading: true,
  user: null,
  error: null
};

const authReducer = (state = initialState, action) => {
  const { type, payload } = action;

  switch (type) {
    case USER_LOADED:
      return {
        ...state,
        isAuthenticated: true,
        loading: false,
        user: payload
      };
    case LOGIN_SUCCESS:
    case SIGNUP_SUCCESS:
      localStorage.setItem('token', payload.token);
      return {
        ...state,
        ...payload,
        isAuthenticated: true,
        loading: false
      };
    case LOGOUT:
    case AUTH_ERROR:
    case LOGIN_FAIL:
    case SIGNUP_FAIL:
      localStorage.removeItem('token');
      return {
        ...state,
        token: null,
        isAuthenticated: false,
        loading: false,
        user: null,
        error: payload
      };
    // other cases
    default:
      return state;
  }
};

export default authReducer;
